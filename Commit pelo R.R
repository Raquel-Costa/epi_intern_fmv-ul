#Guardar ficheiros pelo R diretamente:

  #1. Guardar ficheiro no pc na pasta que está ligada ao Github que neste caso é a prep-estagio-epi
  #2. Fazer commit do ficheiro, fazendo descrição o ficheiro no espaço em branco e carregando em commit
  #3. Fazer pull se o R não estiver com todos os ficheiros atualizados do github
  #4. Fazer push para o ficheiro que queremos ficar guardado no github
