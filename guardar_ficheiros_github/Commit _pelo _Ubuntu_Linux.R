#Guardar no Github pelo Ubuntu:

  #1. Guardar ficheiro R no pc
  #2. Abrir o explorer.exe . no Ubuntu e copiar o ficheiro do pc para a pasta que quero no ubuntu
  #3. Se quiser mudar um ficheiro que já está no Ubuntu posso abrir só pelo explorer.exe . e guardar
  #4. No ubuntu ir para a pasta que quero ex. cd ~/estagio_epi/prep-estagio-epi
  #5. git add .
  #6. git commit -m "descrição do ficheiro"
  #7. git push